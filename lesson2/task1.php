<?php

	$a = rand(-10,10);
	$b = rand(-10,10);

	echo "a = $a,<br>b = $b"."<hr>";



	if ($a >= 0 && $b >= 0) :?>
		<h2>Оба числа положительные. a - b = <?=$a - $b?></h2>
	<?php
	elseif ($a < 0 && $b < 0) :?>
		<h2>Оба числа отрицательные. a * b = <?=$a * $b?></h2>
	<?php
	else :?>
		<h2>Числа разных знаков. a + b = <?=$a + $b?></h2>
	<?php
	endif;

?>